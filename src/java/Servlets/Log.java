package Servlets;


public class Log {
    
    private String usu;
    private String pass;
    private int permiso;

    public void setUsu(String usu) {
        this.usu = usu;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public void setPermiso(int permiso) {
        this.permiso = permiso;
    }

    public String getUsu() {
        return usu;
    }

    public String getPass() {
        return pass;
    }

    public int getPermiso() {
        return permiso;
    }
    
 


   
}
